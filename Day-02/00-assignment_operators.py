"""
Python has 3 types of Numbers:
  - Integer Numbers
  - Float Numbers
  - Complex Numbers
"""
x = 10

x += 10  # x = x + 10
print(x)

x -= 10  # x = x - 10
print(x)

x *= 10  # x = x * 10
print(x)

x **= 10  # x = x ** 10 => x power 10
print(x)
