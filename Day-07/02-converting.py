#!/usr/bin/python3

# boolean to string
print(str(True))  # a word called 'True'
print(str(False))  # a word called 'False'

# boolean to integer
print(int(True))  # 1
print(int(False))  # 0

print(5 + True)  # 5 + 1 = 6
print((5 * False))  # 5 * 0 = 0

# print('mad' + 2)  # TypeError: can only concatenate str (not "int") to str
